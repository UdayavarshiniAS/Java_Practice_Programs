package ex_02_Java_Basics;

public class Lab020_Datatype {
    public static void main(String[] args){

        //byte, short, int, long -------------> numbers without decimal

        byte age=65;
        System.out.println("Age :" +age);

        int num=129;
        System.out.println("Num value : "+num);

        short sh=3535;
        System.out.println("Sh value : "+sh);

        Long l=1234567897L;
        System.out.println("Long value : "+l);

        //float and double for decimal values

        float f=15.4567870898f;
        System.out.println("Float value : "+f);

        double db=34567.98765432112341234567;
        System.out.println("Double value : "+db);

        //char datatype to print single character

        char ch='U';
        System.out.println("Char value : "+ch);

        //boolean datatype to hold true or false

        boolean b1= true;
        System.out.println("Boolean value : " +b1);





    }
}

package ex_02_Java_Basics;

public class Lab011_Common_Math {

    public static void main(String[] args)
    {
        System.out.println(2+2);
        System.out.println(2-2);
        System.out.println(2*2);
        System.out.println(2/2);
        System.out.println("2+2");
        //if we use double quotes, it will be considered as a string and will be printed as it is
        System.out.println(3/2);
        //println function always returns integer even if the above ans is 1.5 - but only 1 will be printed
    }


}
